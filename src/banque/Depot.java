    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String compteno = txtcmp.getText();
        try {
            // TODO add your handling code here
            
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3307/banque","root","");
            insert = con.prepareStatement("SELECT c.client_id, c.nom, c.prenom, a.solde FROM clients c, compte a WHERE c.client_id = a.client_id and a.compte_id = ?");
            insert.setString(1, compteno);
            rs = insert.executeQuery();
            
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(null, "Ce compte n'existe pas !");
                txtcmp.setText("");
                jLabel6.setText("");
                jLabel7.setText("");
                jLabel11.setText("");
            }
            
            else
            {
                String id = rs.getString(1);
                String nom = rs.getString(2);
                String prenom = rs.getString(3);
                String solde = rs.getString(4);
                
                jLabel5.setText(id.trim());
                jLabel6.setText(nom.trim());
                jLabel7.setText(prenom.trim());
                jLabel11.setText(solde.trim());
            
            }
            
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Depot.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

