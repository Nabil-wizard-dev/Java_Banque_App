    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            
            con.setAutoCommit(false);
            String compteno = txtcmp.getText();
            String client_id = jLabel5.getText();
            //String nom = jLabel6.getText();
            //String prenom = jLabel7.getText();
            String date = jLabel8.getText();
            String solde = jLabel11.getText();
            String montant = txtmontant.getText();
            
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3307/banque","root","");
            
            String queryco1 = "insert into depots(compte_id, client_id, date, solde, depot)values(?,?,?,?,?)";
            insert = con.prepareStatement(queryco1);
            insert.setString(1, compteno);
            insert.setString(2, client_id);
            insert.setString(3, date);
            insert.setString(4, solde);
            insert.setString(5, montant);
            insert.executeUpdate();
            
            
            String sqlupdate = "update compte set solde = solde+ ? WHERE compte_id=?";
            insert2 = con.prepareStatement(sqlupdate);
            insert2.setString(1, montant);
            insert2.setString(2, compteno);
            insert2.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Votre compte a été créditer avec succès !");
            txtcmp.setText("");
            jLabel5.setText("");
            jLabel8.setText("");
            jLabel11.setText("");
            txtmontant.setText("");
            con.commit();
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Depot.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex){
            try {
                Logger.getLogger(Depot.class.getName()).log(Level.SEVERE, null, ex);
                con.rollback();
            } catch (SQLException ex1) {
                Logger.getLogger(Depot.class.getName()).log(Level.SEVERE, null, ex1);
            }    
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

