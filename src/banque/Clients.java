    public void autoID()
    {
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3307/banque","root","");
            Statement s = con.createStatement();
            
            ResultSet rs = s.executeQuery("SELECT MAX(client_id) FROM clients");
            rs.next();
            rs.getString("MAX(client_id)");
            
            
            if(rs.getString("MAX(client_id)")==null)
            {
                
                jLabel11.setText("WK001");
            
            }
            
            else
            {
                
                long id = Long.parseLong(rs.getString("MAX(client_id)").substring(2, rs.getString("MAX(client_id)").length()));
                id++;
                jLabel11.setText("WK" + String.format("%03d", id));

            
            }
    
            
        } 
        
        catch (ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
            //Logger.getLogger(Clients.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
    }
    
  

