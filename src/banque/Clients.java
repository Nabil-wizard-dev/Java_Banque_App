    public boolean existsInBranch(Branch elt){
        for(Branch o: this.branchs){
            if(elt.id == o.id) return true;
        }
        return false;
    }
    
    public Branch getBranchByNom(String nom){
        for(Branch o: this.branchs){
            if(nom == o.nom) return o;
        }
        return null;
    }
    
    public void ChoixBranche()
    {
        try{
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3307/banque","root","");
            insert = con.prepareStatement("SELECT * FROM branches");
            ResultSet rs = insert.executeQuery();
            txtbranche.removeAllItems();
            while(rs.next())
            {
                Branch b = new Branch (rs.getInt(1), rs.getString(2) );
                if(!this.existsInBranch(b)) this.branchs.add(b);
//                txtbranche.addItem((new Branch (rs.getInt(1), rs.getString(2) )).toString() );
            }
            for(Branch o: this.branchs) txtbranche.addItem(o.toString() );
        } 
        catch (Exception e){
        
        }
    }
    
