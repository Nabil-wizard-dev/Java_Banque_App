    public void load()
    {                   
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3307/banque","root","");
            insert = con.prepareStatement("SELECT * FROM utilisateur WHERE username=? and password=? ");                           
            insert.setString(1, txtuser.getText());
            insert.setString(2,txtpass.getText());
            rs=insert.executeQuery();
        if(rs.next())
        {
          principal c = new principal();
          this.hide();
          c.setVisible(true);           
       }
      else
       {
         JOptionPane.showMessageDialog(null, "Le nom d'utilisateur et le mot de passe ne correspondent pas !");
         txtuser.setText("");
         txtpass.setText("");
         txtuser.requestFocus();
        }               
                       
        } catch (Exception e) {
            
              System.out.println("Connection Echouée " + e);
            
        }             
    }
      
